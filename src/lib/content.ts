/**
 * Композиция текстов хабов услуг и городов из данных (DRY). Топоним — в естественном
 * падеже из cities.ts. Тон — профессиональный и уверенный.
 */
import type { City, Service, Faq } from '../data/types';

/* ---------- Хаб услуги ---------- */

export function serviceHubTitle(service: Service): string {
  return `${service.nameNom} — Spectrum Guard | по всей России`;
}

export function serviceHubDescription(service: Service): string {
  return (
    `${service.nameNom}: ${service.tagline.toLowerCase()}. Что входит, как заказать, сколько ` +
    `стоит. Spectrum Guard — профессиональная охрана и безопасность по всей России. ` +
    `Оставьте заявку или напишите в Telegram.`
  );
}

/* ---------- Хаб города ---------- */

export function cityHubH1(city: City): string {
  return `Охрана и безопасность ${city.prep}`;
}

export function cityHubTitle(city: City): string {
  return `Охрана ${city.prep} — Spectrum Guard`;
}

export function cityHubDescription(city: City): string {
  return (
    `Охрана и личная безопасность ${city.prep}: личная охрана, защита при угрозах, охрана при ` +
    `переезде, помощь с соседями, охрана мероприятий и перевозка ценных грузов. Spectrum Guard — ` +
    `профессиональная команда и полная конфиденциальность. Оставьте заявку.`
  );
}

/** Городской FAQ — профессионально и по делу. */
export function cityFaq(city: City): Faq[] {
  return [
    {
      q: `Вы работаете ${city.prep}?`,
      a:
        `Да. ${city.nom} — один из городов, где работает Spectrum Guard: личная охрана, охрана при ` +
        `переезде, защита при угрозах, помощь при конфликте с соседями, охрана мероприятий и ` +
        `перевозка ценных грузов. По запросу выезжаем и в другие регионы.`,
    },
    {
      q: `Как заказать охрану ${city.prep}?`,
      a:
        `Оставьте заявку на сайте или напишите в Telegram — коротко опишите задачу и город. ` +
        `Мы свяжемся, уточним детали, и заявку возьмёт в работу команда ${city.prep}: согласуем ` +
        `формат, сроки и стоимость.`,
    },
    {
      q: `Сколько стоит охрана ${city.prep}?`,
      a:
        `Стоимость зависит от задачи: формата (разовый выезд или постоянный режим), числа ` +
        `специалистов и длительности. Точную цену называем после короткого разговора, без скрытых доплат.`,
    },
  ];
}

